import { isTextFile, listFiles, readText, fail } from './check-utils.mjs';

const platformTerms = [
  'self-hosted',
  'NPM_TOKEN',
  'FALLBACK_NODE_AUTH_TOKEN',
  'mirror-publish',
  'mirror publish',
  'gitlab',
  'azure',
  ['dop', 'pler'].join(''),
  'railway',
  'fly.io',
  'vercel',
  'netlify',
  'kubernetes',
  'k8s',
  'helm',
  'warpdotdev',
  'warp.dev',
  'continue-on-error',
];
const hypePhrases = [
  'best in class',
  'world-class',
  'industry-leading',
  'cutting-edge',
  'state of the art',
  'perfect',
  'flawless',
  'principal developer as quality claim',
  'principle developer',
  'professional grade',
  'enterprise grade',
  'production grade',
  'production-ready',
  'production-oriented',
  'security-hardened',
  'missing infrastructure layer',
  'robust solution',
  'powerful tool',
  'amazing',
  'unleash',
  'supercharge',
  'revolutionize',
  'skyrocket',
  'delight',
  'as you can see',
  'needless to say',
  'without further ado',
  'in conclusion',
  'for everyone',
  'trusted by thousands',
  'Generated by',
  'AI-generated',
  'Co-authored-by: Claude',
  'Co-authored-by: GPT',
  'Co-authored-by: Codex',
];
const exactAllowed = 'It is not an official Google, Linux Foundation, or a2aproject package.';
const ignored = [
  /^AGENTS\.md$/,
  /^scripts\/check-.*\.mjs$/,
  /^pnpm-lock\.yaml$/,
  /^docs\/audits\//,
  /(^|\/)LICENSE$/,
  /(^|\/)LICENSES\//,
];
const failures = [];
for (const file of listFiles()) {
  if (!isTextFile(file)) continue;
  if (ignored.some((rule) => rule.test(file))) continue;
  let text = readText(file).split(exactAllowed).join('');
  const lower = text.toLowerCase();
  for (const term of platformTerms) {
    if (lower.includes(term.toLowerCase())) {
      failures.push(`${file}: ${term}`);
    }
  }
  for (const phrase of hypePhrases) {
    if (lower.includes(phrase.toLowerCase())) failures.push(`${file}: ${phrase}`);
  }
}
if (failures.length > 0) fail('Forbidden references found.', [...new Set(failures)].slice(0, 120));
